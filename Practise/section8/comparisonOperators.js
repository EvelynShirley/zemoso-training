//Comparison Operators
//== :equalto
//=== :equalto with type
// != : notequal to
//< : less than
// > : greater than
// <= :less than or equal to
// >= : greater than or equal to
console.log(1==1);
console.log(1==2);
console.log(1===1);
console.log(1=="1");
console.log(1==="1"); // compares type (here numeric and string type)
console.log(1!=1);
console.log(1!==1);
console.log(1!=="1"); // compares type (here numeric and string type)
console.log(1<1);
console.log(1<2);
console.log(1>0);
console.log(1<=2);
console.log(1<=0);
